package Departamentos;
public class Departamentos {
    private int deptoCodigo;
    private String deptoSigla,deptoNome,deptoDiretor;


    public int getDeptoCodigo() {
        return deptoCodigo;
    }

    public void setDeptoCodigo(int deptoCodigo) {
        this.deptoCodigo = deptoCodigo;
    }

    public String getDeptoSigla() {
        return deptoSigla;
    }

    public void setDeptoSigla(String deptoSigla) {
        this.deptoSigla = deptoSigla;
    }


    public String getDeptoNome() {
        return deptoNome;
    }
    public void setDeptoNome(String deptoNome) {
        this.deptoNome = deptoNome;
    }

    public String getDeptoDiretor() {
        return deptoDiretor;
    }

    public void setDeptoDiretor(String deptoDiretor) {
        this.deptoDiretor = deptoDiretor;
    }
}
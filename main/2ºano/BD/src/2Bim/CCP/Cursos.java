package CCP;

public class Cursos {
    private String codigo, nome, carga, tipo, modalidade;
    public String getCodigo() {
        return codigo;
    }
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    public String getCarga() {
        return carga;
    }
    public void setCarga(String diretor) {
        this.carga = diretor;
    }
    public String getTipo() {
        return tipo;
    }
    public void setTipo(String ano) {
        this.tipo = ano;
    }
    public String getModalidade() {
        return modalidade;
    }
    public void setModalidade(String pais) {
        this.modalidade = pais;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String titulo) {
        this.nome = titulo;
    }
}